START:	MVI  A, 50H
		OUT  LATCH
		CALL DELAY1
		MVI  A, 74H
		OUT  LATCH
		CALL DELAY2
		JMP  START
		RST  1

DELAY1: LXI  H, 3FFFH		; ON TIME DELAY
LOOP1:	DCX  H
		MOV  A, L
		ORA  H		
		JNZ  LOOP1
		RET

DELAY2: LXI  H, BFFDH		; OFF TIME DELAY
LOOP2:	DCX  H
		MOV  A, L
		ORA  H		
		JNZ  LOOP2
		RET		